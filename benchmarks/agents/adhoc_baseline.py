# ---------------------------------------------------------------------------
# Ad-hoc rules — generated by prompt engineering intuition alone.
# NO Covenant Framework, Canon, or governance principles referenced.
# ---------------------------------------------------------------------------
ADHOC_PROMPT = textwrap.dedent("""\
You are a coding agent solving terminal-based programming tasks. Follow these rules strictly:

1. Read the entire task description carefully before writing any code. Identify the exact input/output format, edge cases, and success criteria before touching the keyboard.

2. If a command fails, read the error. Diagnose the root cause. Never run the same failing command twice — change your approach based on what the error tells you.

3. Write small, testable increments. After each meaningful chunk of code, run it against a simple test case to catch errors early rather than debugging a large monolithic program.

4. When stuck for more than two failed attempts on the same approach, step back and reconsider your algorithm or architecture. A different strategy often costs less than patching a broken one.

5. Use the simplest language and approach that solves the problem. Prefer Python for most tasks unless the task specifies otherwise or performance requirements demand C/C++/Rust.

6. Before declaring completion, run the program against all provided test cases and at least one edge case you invent yourself. If no test cases are provided, construct representative inputs from the task description and verify outputs manually.
""")


# ---------------------------------------------------------------------------
# Agent definition — ad-hoc workflow, no framework reference
# ---------------------------------------------------------------------------
ADHOC_AGENT_DEF = textwrap.dedent("""\
---
name: adhoc-bench
description: >
  Ad-hoc baseline benchmark agent. Pure prompt engineering, no framework.
tools: Bash, Read, Write, Edit, Glob, Grep
---

# Ad-Hoc Benchmark Agent

You solve terminal-based programming tasks efficiently and correctly.

## Workflow
1. **Read** — Read the task fully, extract requirements, constraints, I/O specs, identify existing files
2. **Plan** — Choose language, algorithm, data structures, identify the hardest subproblem
3. **Implement** — Write core logic first, test it, then add parsing/formatting/edge-cases
4. **Test** — Test against all provided examples and at least one self-generated edge case
5. **Verify** — Final read-through of task description, confirm nothing missed, run complete solution

## Rules
- Read the full task before writing any code.
- Diagnose errors from their output. Never repeat a failed command.
- Write and test in small increments, not monolithic blocks.
- If stuck after 2 failures, change your entire approach.
- Use the simplest language that solves the problem.
- Test with provided cases AND your own edge case before finishing.""")


class AdHocAgent(ClaudeCode):
    """Ad-hoc baseline agent — prompt engineering without governance framework.

    No retry. Single attempt with ad-hoc rules only.
    This is Group B in the controlled experiment.
    """

    @staticmethod
    def name() -> str:
        return "adhoc-baseline"

    def version(self) -> str:
        return "1.1.0"

    async def install(self, environment: BaseEnvironment) -> None:
        await super().install(environment)

        # Single combined command — mirrors covenant agent optimization
        await self.exec_as_agent(
            environment,
            command=(
                f"mkdir -p /app/.claude/agents && "
                f"cat > /app/.claude/agents/adhoc-bench.md << 'AGENT_EOF'\n"
                f"{ADHOC_AGENT_DEF}\n"
                f"AGENT_EOF\n"
                f"cat > /app/CLAUDE.md << 'CLAUDE_EOF'\n"
                f"# Project Instructions\n"
                f"\n"
                f"Solve the task in this directory. Read existing files before writing new ones.\n"
                f"Test your solution before finishing. Work efficiently — time is limited.\n"
                f"CLAUDE_EOF"
            ),
        )

    def build_cli_flags(self) -> str:
        base_flags = super().build_cli_flags()
        agent_flag = "--agent adhoc-bench"
        if base_flags:
            return f"{base_flags} {agent_flag}"
        return agent_flag

    async def run(
        self, instruction: str, environment: BaseEnvironment, context: AgentContext
    ) -> None:
        self.append_system_prompt = ADHOC_PROMPT
        await super().run(instruction, environment, context)
